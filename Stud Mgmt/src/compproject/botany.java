    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        previous.setEnabled(false);
        next.setEnabled(true);

        pic1_pan.setVisible(true);
        pic2_pan.setVisible(true);
        jPanel1.setVisible(true);
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/skool","root","mysqlip");
             Statement stm=(Statement)conn.createStatement();

             String x=(String) searchCB.getSelectedItem();
             String nm=searchTxt.getText();

             if(x.equals("Common Name")){

                 String sql2="Select count(*) from botany where c_name like '%"+nm+"';";

                 rsc1=stm.executeQuery(sql2);
                 rsc1.next();
                 count=rsc1.getInt("count(*)");
                 
                 String sql1="Select genus,species from botany where c_name like '%"+nm+"';";

                 rs1=stm.executeQuery(sql1);
                 rs1.next();
                 String gen=rs1.getString("Genus");
                 String spec=rs1.getString("Species");
                 smsearch(gen,spec);
             }
             
             else if(x.equals("Location")){
                 String sql2="Select count(*) from botany where location like '%"+nm+"%';";

                 rsc1=stm.executeQuery(sql2);
                 rsc1.next();
                 count=rsc1.getInt("count(*)");

                 String sql1="Select genus,species from botany where location like '%"+nm+"%';";
                 rs1=stm.executeQuery(sql1);
                 rs1.next();
                 String gen=rs1.getString("Genus");
                 String spec=rs1.getString("Species");
                 smsearch(gen,spec);
             }

             else if(x.equals("Description")){
                  String sql2="Select count(*) from botany where Description like '%"+nm+"%';";

                 rsc1=stm.executeQuery(sql2);
                 rsc1.next();
                 count=rsc1.getInt("count(*)");

                 
                 String sql1="Select genus,species from botany where Description like '%"+nm+"%';";
                 rs1=stm.executeQuery(sql1);
                 rs1.next();
                 String gen=rs1.getString("Genus");
                 String spec=rs1.getString("Species");
                 smsearch(gen,spec);
             }

             if(count==1)
                   next.setEnabled(false);

                ncount=1;
                outcount.setText(""+count);
                nowcount.setText(""+ncount);

                rsc1.close();
             
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,ex.getLocalizedMessage());
            jPanel1.setVisible(false);
        }

    }//GEN-LAST:event_searchBtnActionPerformed

