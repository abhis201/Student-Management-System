    public void smsearch(String genus,String species){
 
            try {

             Class.forName("com.mysql.jdbc.Driver");
             Connection con=(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/skool","root","mysqlip");
             Statement stmt=(Statement)con.createStatement();

             String sql1="Select * from botany where genus like '"+genus+"' and species like '"+species+"';";
             ResultSet rs=stmt.executeQuery(sql1);

             rs.next();
             String c_name=rs.getString("c_name");
             String king=rs.getString("Kingdom");
             String div=rs.getString("Division");      
             String cls=rs.getString("Class");
             String order=rs.getString("bot_order");
             String family=rs.getString("Family");
             String gen=rs.getString("Genus");
             String spec=rs.getString("Species");

             cname.setText(c_name);
             kingdom.setText(king);
             division.setText(div);
             cla.setText(cls);
             orderTxt.setText(order);
             fmlyTxt.setText(family);
             genusTxt.setText(gen);
             speciesTxt.setText(spec);
             sname.setText(gen+" "+spec);

             
             
             String loc=rs.getString("location");
             String desc=rs.getString("Description");
             String bio_fact=rs.getString("bio_fact");
             String Medi_qual=rs.getString("medi_qual");
             String yr=rs.getString("yr_plant");

             locTA.setText(loc);
             descTA.setText(desc);
             factTA.setText(bio_fact);
             mediqTA.setText(Medi_qual);
             yrTA.setText(yr);

             InputStream in1 = rs.getBinaryStream("pic_skool");
            InputStream in2 = rs.getBinaryStream("pic_out");

            if(in1!=null){
             pic1_pan.setVisible(true);
             img1=ImageIO.read(in1);
             Image toolkit1=img1.getScaledInstance(300,190,Image.SCALE_SMOOTH);
             BufferedImage newImage1=new BufferedImage(300,190,BufferedImage.TYPE_INT_ARGB);
             Graphics g1=newImage1.getGraphics();
             g1.drawImage(toolkit1,0,0,null);
             pic1.setIcon(new ImageIcon(newImage1));
            }
            else{
                pic1_pan.setVisible(false);
            }

            if(in2!=null){
             pic2_pan.setVisible(true);
             img2=ImageIO.read(in2);
             Image toolkit2=img2.getScaledInstance(300,190,Image.SCALE_SMOOTH);
             BufferedImage newImage2=new BufferedImage(300,190,BufferedImage.TYPE_INT_ARGB);
             Graphics g2=newImage2.getGraphics();
             g2.drawImage(toolkit2,0,0,null);
             pic2.setIcon(new ImageIcon(newImage2));
            }
            else{
                pic2_pan.setVisible(false);
            }

            }

            catch (Exception ex) {
                JOptionPane.showMessageDialog(null,ex.getLocalizedMessage());
            }

                 }

